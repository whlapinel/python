# Practice Problems: If Statements

# 1. Divisibility Test
# Write a program that takes a number and checks if it is divisible by both 3 and 5.

# 2. Largest of Three Numbers
# Write a program that takes three numbers and prints the largest.

# 3. Grading System
# Write a program that takes a numerical grade (0-100) and prints the letter grade:
# 90-100: A, 80-89: B, 70-79: C, 60-69: D, below 60: F.

# 4. Leap Year Checker
# Write a program that takes a year as input and checks if it is a leap year.

# 5. Triangle Type
# Write a program that takes three side lengths and determines if they form an equilateral, isosceles, or scalene triangle.
